#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/** \brief recibe dos numeros y calcula la suma.
 *
 * \param n1 es el primer numero ingresado por el usuario.
 * \param n2 es el segundo numero ingresado por el usuario.
 * \return la suma de los numeros.
 *
 */

float suma (float numero1,float numero2);

/** \brief recibe dos numeros y calcula la resta de ellos.
 *
 * \param n1 es el primer numero ingresado por el usuario.
 * \param n2 es el segundo numero ingresado por el usuario.
 * \return la resta de los numeros.
 *
 */

float resta (float numero1,float numero2);

/** \brief recibe dos numeros y los multiplica.
 *
 * \param n1 es el primer numero ingresado por el usuario.
 * \param n2 es el segundo numero ingresado por el usuario.
 * \return el producto de los numeros.
 *
 */

float multiplicacion (float numero1,float numero2);

/** \brief recibe dos numeros y calcula la division de ellos.
 *
 * \param n1 es el primer numero ingresado por el usuario.
 * \param n2 es el segundo numero ingresado por el usuario.
 * \return la division de los numeros.
 *
 */

float division (float numero1,float numero2);

/** \brief multiplica el numero ingresado, por sus numeros anteriores hasta llegar al numero uno.
 *
 * \param n1 es el primer numero ingresado por el usuario.
 * \return el factorial del numero ingresado.
 *
 */

float factorial(float numero1);

#endif // FUNCIONES_H_INCLUDED

